<h1>La Semaine d'Intégration</h1>
<h2>La journée BDE - le mardi 6 Septembre 2011</h2>
<p> Ta journée débutera par une présentation de l'association des anciens élèves. Cette association te permettra entre autre de d'adhérer au grand réseau des anciens et peut-être d'y trouver ton stage ou même un contrat d'embauche! </p>
<p>L'après-midi sera ponctué par des activités organisées par le BDE Hypnoz. Yu intégreras une des 7 équipes d’étudiants de ta promo et tu participeras à un grand concours de déguisements.</p>
<p>Plusieurs thèmes te seront donnés, et ce sera à toi de faire preuve d’imagination et d’originalité pour faire gagner ton équipe ! L’ambiance sera à son comble durant cette journée, alors viens découvrir ta promo en t’éclatant !</p>

<p>Le soir tu pourras découvrir la vie associative de ton campus. En effet, tous les clubes & associations se réunissent au foyer pour te proposer de découvrir leurs accomplissement. C'est l'occasion pour toi de faire tes premiers choix associatifs!</p>

<h2>La journée ASINT - le mercredi 7 Septembre 2011</h2>
<p>La matinée sera consacrée aux tests de langues qui se déroulera dans les salles d'examen du campus</p>
<p>Après une matinée studieuse, il est temps pour toi de te dépenser! L’ASINT (Association Sportive de l’INT - ancien nom de l'école) te propose de revenir pour t’amuser une fois de plus. Activités en tous genres, baby-foot géant, ballon prisonnier, concours délirants...Une multitude d’occasions pour profiter à fond de ta semaine et t’éclater une nouvelle fois avec ta promo !</p>
<p> Le soir tu assisteras à ton premier Club B - le club de dégustation de bière du campus. L'occasion pour toi de discuter avec ta promo autour d'une bière et de faire connaissance avec les vieux, si ce n'est pas déjà fait!</p>

<h2>La journée BDA - le jeudi 8 Septembre 2011</h2>
<p>Pour bien terminer cette Semaine d’Intégration, le BDA (Bureau des Arts) organise un max d’activités en rapport avec les arts. Concours de cuisine, chorégraphies, poèmes, viens exprimer tes talents pour en mettre plein la vue à ta promo et folâtrer avec ton équipe, pour une ambiance de folie assurée !
De plus, un jeu de piste en équipe te permettra de découvrir les endroits les plus insolites du campus!</p>
<p>Le soir, la dernier soirée de ta semaine d'intégration s'offre à toi. Organisée par le BDA et l'ASINT, viens terminer ta semaine autour du théme "Asterix aux jeux olympix". A vos marques! Prêts? Partez!
