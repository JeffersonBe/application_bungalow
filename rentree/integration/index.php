<h1>Le mot du tresorier</h1>
<img src="img/rabaultcat.jpg" style="width:250px ; height:auto;" title="Pierre, notre Trez!"/><p>Le WEI. Encore pour toi un simple acronyme un peu trouble. Sauf que tu te rendras compte très vite que derrière se cache, de l'avis de tous les étudiants passé par l'INT, le week-end le plus mémorable, le plus rock & roll de ta scolarité. Comme entrée en matière dans le monde de la nuit INTienne, la seule drogue qui vaille pour tenir toute une scolarité à Evry, tu ne trouveras pas mieux. Après deux années de sevrage imposé, ton appétit sexuel est surement débordant. De ce week-end, tu sortiras assurément rassasié. Mais tu oublies LA chose qui fâche. Goûter à tous ces plaisirs ne te dispense pas d'y mettre le prix. Eh oui mon ami(e)...</p>
<br />
<p>Que tu sois vite rassuré(e), ce n'est rien de prohibitif. Seulement, quelques éclaircissements s'imposent. 
Résumons. Dès ton arrivée à la Maisel, surement entre le vendredi 2 septembre et le dimanche 4 septembre, tu trouveras tous les stands indispensables si tu veux partir au WEI : le stand BDE et le stand Société Générale. La présence du BDE ne te surprendra guère, ce qui n'est peut-être pas le cas de celui d'une banque. La raison est très simple. Grâce au partenariat qui unit le BDE de TEM et TSP et la Société Générale tu pourras bénéficier jusqu'à 100 € d'économie et même bien plus si tu es boursier. Tu découvriras une offre qui est tout spécialement dédiée au étudiants de TEM et TSP (disponible en format pdf ci dessous). 
</p>
<br />
<p>
Véritable passeport pour la vie associative et surtout pour ton départ au WEI, la cotisation BDE, valable toute ta scolarité, te coutera 150 euros. A l’ouverture d’un compte à la Société Générale elle ne te fera débrousser plus que 120 euros. Une fois ta cottiz en poche le WEI s’offre à toi pour 250 euros, ou 180 euros si tu décides de profiter du partenariat. En somme, 400 euros te permettront de partir au WEI. 300 euros avec la Société Générale (au passage tu peux déjà prononcer Sogé).</p>
<br />
<p>
Au vu de la forte proportion de boursiers lors de chaque rentrée, il est tout naturel que le BDE propose des tarifs avantageux aux boursiers. Là encore, c’est la Société Générale qui t’en fait profiter. Les prix proposés le sont en fonction des paliers fixés par le Crous. Ainsi pour les paliers 1 à 4, l’inscription pour le WEI est à 120 euros. En ajoutant la cotisation BDE, tu paieras au total 250 euros. 
Enfin, pour les paliers 5 et 6, le WEI est à 30 euros seulement ! Soit un total de 110 euros.</p>
<br />
<p>Sache que l’ouverture d’un compte n’est en rien obligatoire, mais en plus d’avantages tarifaires elle permet de faire vivre le monde associatif sur le campus. 
Une dernière chose, une conseillère de la Société Générale sera présente tous les jeudi sur le campus. Tu pourras lui poser tes questions, déposer des chèques et réaliser toutes sortes d’opérations. </p>
<br />
<p>En attendant de te retrouver sur le campus à la rentrée, la trésorerie te félicite pour ton intégration et te souhaite une très bonne fin de vacances !</p	>
